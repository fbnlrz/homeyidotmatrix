/**
 * Map handler: looks up the glyph by character. Lowercase → uppercase fallback.
 * Returns a 5x7 row array.
 */
function getGlyph(ch) {
  if (G[ch]) return G[ch];
  const upper = ch.toUpperCase();
  if (G[upper]) return G[upper];
  return G[' '];
}

/**
 * Pre-render the 5x7 font into a 16-byte-per-row 8x16 buffer map.
 * We give the protocol a `font` object shaped like { [charCode]: Buffer(16) }
 * where each byte is one row, MSB = leftmost pixel of the 8-wide glyph.
 * The protocol's renderer then scales 2x to fill the 16x32 character cell.
 */
const font = {};
for (const ch of Object.keys(G)) {
  const glyph = G[ch];
  const buf = Buffer.alloc(16);
  // Center the 5x7 glyph inside the 8x16 cell: 2px left pad, 5px top pad.
  for (let r = 0; r < 7; r++) {
    const row = glyph[r] & 0x1f; // 5 bits
    // shift right by (8 - 5 - leftPad)? leftPad=2 → glyph occupies bits 5..1
    // Place 5-bit row into 8-bit byte at column 2 (bit position 5..1, MSB=bit7)
    const shifted = row << 1; // bits 5..1
    buf[5 + r] = shifted & 0xff;
  }
  font[ch.charCodeAt(0)] = buf;
}

// Also map lowercase to uppercase glyphs.
for (let c = 97; c <= 122; c++) {
  const upper = String.fromCharCode(c - 32);
  if (G[upper]) font[c] = font[upper.charCodeAt(0)];
}

module.exports = font;
module.exports.getGlyph = getGlyph;
