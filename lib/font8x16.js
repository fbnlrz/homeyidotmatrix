function getGlyph(ch) {
  if (G[ch]) return G[ch];
  const upper = ch.toUpperCase();
  if (G[upper]) return G[upper];
  return G[' '];
}

// Pre-render the 5×7 font into the 8×16 grid the protocol's text renderer
// scales up. Each entry is a 16-byte buffer where each byte is one row,
// MSB = leftmost pixel of the 8-wide glyph.
const font = {};
for (const ch of Object.keys(G)) {
  const glyph = G[ch];
  const buf = Buffer.alloc(16);
  for (let r = 0; r < 7; r++) {
    const row = glyph[r] & 0x1f;
    // Center the 5-wide glyph at columns 2..6 of the 8-wide cell, with 5px top pad.
    buf[5 + r] = (row << 1) & 0xff;
  }
  font[ch.charCodeAt(0)] = buf;
}

// Map lowercase ASCII to its uppercase fallback if no proper lowercase glyph.
for (let c = 97; c <= 122; c++) {
  if (font[c]) continue;
  const upper = String.fromCharCode(c - 32);
  if (G[upper]) font[c] = font[upper.charCodeAt(0)];
}

module.exports = font;
module.exports.getGlyph = getGlyph;
